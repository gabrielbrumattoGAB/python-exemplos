alunos = ['Adriana', 'João', 'Maria', 'Pedro', 'Ana', 'Lucas', 'Mariana', 'Adriana']
print(f"Lista de alunos: {alunos}")
# Método Remove
alunos.remove('Adriana')
print(f"Lista de alunos: {alunos}")
# Método Pop
alunos.pop(2)
print(f"Lista de alunos: {alunos}")
# Função del
del alunos [3]
print(f"Lista de alunos: {alunos}")
# Método clear
alunos.clear()
print(f"Lista de alunos: {alunos}")